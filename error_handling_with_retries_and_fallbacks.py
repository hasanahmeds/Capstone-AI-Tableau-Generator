@dataclass
class PipelineResult:
    """
    A "never crash" result container:
    - df/schema/quality/profile might be None if that stage fails
    - errors/warnings accumulate for UI display (Streamlit etc.)
    """
    df: Optional[pd.DataFrame] = None
    schema: Optional[Any] = None
    quality: Optional[Any] = None
    profile: Optional[Any] = None
    warnings: list[str] = None
    errors: list[str] = None

    def __post_init__(self) -> None:
        if self.warnings is None:
            self.warnings = []
        if self.errors is None:
            self.errors = []


class ResilientPipeline:
    """
    Wraps your DataProcessor with retries + fallbacks.

    Typical usage:
        rp = ResilientPipeline(processor=DataProcessor())
        result = rp.run("train.csv", dataset_name="Train Dataset")
        # result.schema / result.quality / result.profile ready for downstream steps
    """

    def __init__(self, processor: Any):
        self.processor = processor

        # Policies per stage (tune as you like)
        self.load_policy = RetryPolicy(
            max_attempts=3,
            base_delay_s=0.5,
            max_delay_s=6.0,
            backoff=2.0,
            jitter_s=0.3,
            retry_on=(OSError, TimeoutError, ConnectionError),
        )
        self.compute_policy = RetryPolicy(
            max_attempts=2,
            base_delay_s=0.25,
            max_delay_s=2.0,
            backoff=2.0,
            jitter_s=0.2,
            retry_on=(TimeoutError,),  # computation usually shouldn't retry on logic errors
        )

    def run(
        self,
        file_path: Union[str, "pd.io.common.FilePath"],
        *,
        dataset_name: Optional[str] = None,
        source_filename: Optional[str] = None,
        sheet_name: Union[str, int, None] = 0,
    ) -> PipelineResult:
        result = PipelineResult()

        # -------------------------
        # 1) LOAD with fallbacks
        # -------------------------
        try:
            # If you want to use your existing processor.load(), you can wrap that too.
            # But here we provide stronger format-specific fallbacks.
            fp = str(file_path).lower()
            if fp.endswith(".csv"):
                df = safe_read_csv_with_fallbacks(file_path, policy=self.load_policy)
            elif fp.endswith(".xlsx") or fp.endswith(".xls"):
                df = safe_read_excel_with_fallbacks(file_path, sheet_name=sheet_name, policy=self.load_policy)
            else:
                # fallback: try processor.load (it already checks extensions)
                df = retry(lambda: self.processor.load(file_path), policy=self.load_policy, op_name="processor.load")

            # Align with your processor.load() behavior (strip columns)
            df.columns = [str(c).strip() for c in df.columns]
            result.df = df

        except Exception as exc:
            result.errors.append(f"Load failed: {exc}")
            logger.exception("Load stage failed")
            return result  # hard stop (no df -> no pipeline)

        # -------------------------
        # 2) VALIDATE (schema)
        # -------------------------
        try:
            # Validate is mostly deterministic; usually not a good retry candidate
            schema = self.processor.validate(
                result.df,
                source_filename=source_filename,
                dataset_name=dataset_name,
            )
            result.schema = schema

        except Exception as exc:
            # Fallback: build a minimal schema (so UI can still continue)
            result.warnings.append(f"Validate failed; using minimal schema fallback. Reason: {exc}")
            logger.exception("Validate stage failed; falling back to minimal schema")

            result.schema = self._minimal_schema_fallback(
                df=result.df,
                dataset_name=dataset_name or "uploaded_dataset",
                source_filename=source_filename,
            )
# 3) QUALITY (soft fail)
        # -------------------------
        try:
            quality = retry(
                lambda: self.processor.assess_quality(result.df, result.schema),
                policy=self.compute_policy,
                op_name="assess_quality",
            )
            result.quality = quality
        except Exception as exc:
            result.warnings.append(f"Quality assessment skipped due to error: {exc}")
            logger.exception("Quality stage failed (soft)")

        # -------------------------
        # 4) PROFILE (soft fail)
        # -------------------------
        try:
            profile = retry(
                lambda: self.processor.profile(result.df, result.schema),
                policy=self.compute_policy,
                op_name="profile",
            )
            result.profile = profile
        except Exception as exc:
            result.warnings.append(f"Profiling skipped due to error: {exc}")
            logger.exception("Profile stage failed (soft)")

        return result

# Minimal schema fallback
# -------------------------
def _minimal_schema_fallback(*, df: pd.DataFrame, dataset_name: str, source_filename: Optional[str]):
    """
    Minimal schema used only when validate() crashes.
    Keeps your downstream steps alive.

    Requires your Pydantic models to be importable.
    """
    # Import inside to avoid circular imports if needed
    from uuid import uuid4
    from datetime import datetime
    from schemas import DatasetSchema, ColumnSchema  # adjust import path as needed

    cols = []
    for c in df.columns:
        s = df[c]
        non_null = s.dropna()
        sample = non_null.head(5).tolist() if not non_null.empty else []
        cols.append(
            ColumnSchema(
                name=str(c),
                dtype_raw=str(s.dtype),
                semantic_type="unknown",
                missing_count=int(s.isna().sum()),
                missing_ratio=float(s.isna().mean()) if len(s) else 0.0,
                unique_count=int(s.nunique(dropna=True)),
                is_constant=(int(s.nunique(dropna=True)) <= 1),
                is_empty=bool(non_null.empty),
                sample_values=sample,
            )
        )

    preview = df.head(5).where(pd.notna(df), None).to_dict(orient="records")

    return DatasetSchema(
        dataset_id=str(uuid4()),
        name=dataset_name,
        file_format="csv" if (source_filename or "").lower().endswith(".csv") else "excel",
        source_filename=source_filename,
        created_at=datetime.utcnow(),
        row_count=int(df.shape[0]),
        column_count=int(df.shape[1]),
        columns=cols,
        numeric_columns=[],
        categorical_columns=[],
        datetime_columns=[],
        boolean_columns=[],
        text_columns=[],
        unknown_columns=[str(c) for c in df.columns],
        preview_rows=preview,
    )


