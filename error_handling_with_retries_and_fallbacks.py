@dataclass(frozen=True)
class RetryPolicy:
    max_attempts: int = 3
    base_delay_s: float = 0.5       # first sleep
    max_delay_s: float = 8.0        # cap
    backoff: float = 2.0            # exponential growth factor
    jitter_s: float = 0.25          # random +/- jitter added to each delay
    retry_on: Tuple[Type[BaseException], ...] = (TimeoutError, ConnectionError)
    # You can add more, e.g. pandas/IO transient issues:
    # retry_on: Tuple[Type[BaseException], ...] = (TimeoutError, ConnectionError, OSError)


def _sleep_with_backoff(attempt: int, policy: RetryPolicy) -> None:
    # attempt is 1-based
    delay = policy.base_delay_s * (policy.backoff ** (attempt - 1))
    delay = min(delay, policy.max_delay_s)
    jitter = random.uniform(-policy.jitter_s, policy.jitter_s)
    time.sleep(max(0.0, delay + jitter))


def retry(
    fn: Callable[[], Any],
    *,
    policy: RetryPolicy,
    op_name: str,
    on_error: Optional[Callable[[BaseException, int], None]] = None,
) -> Any:
    """
    Run fn() with retries.

    - Retries only exceptions in policy.retry_on
    - Raises final exception if all retries fail
    """
    last_exc: Optional[BaseException] = None

    for attempt in range(1, policy.max_attempts + 1):
        try:
            return fn()
        except policy.retry_on as exc:
            last_exc = exc
            if on_error:
                on_error(exc, attempt)

            # last attempt -> re-raise
            if attempt >= policy.max_attempts:
                logger.error("❌ %s failed after %d attempts: %s", op_name, attempt, exc)
                raise

            logger.warning("⚠️ %s failed (attempt %d/%d): %s. Retrying...",
                           op_name, attempt, policy.max_attempts, exc)
            _sleep_with_backoff(attempt, policy)

    # should never hit
    if last_exc:
        raise last_exc
    raise RuntimeError(f"Unexpected retry() state for {op_name}")

