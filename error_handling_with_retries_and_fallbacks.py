class LLMError(Exception):
    """Raised for LLM failures (wrap provider exceptions into this)."""


def resilient_generate_tableau_spec(
    llm_call: Callable[[], dict],
    *,
    cache_get: Optional[Callable[[], Optional[dict]]] = None,
    cache_set: Optional[Callable[[dict], None]] = None,
) -> dict:
    """
    Retries + fallbacks for the LLM step that generates Tableau JSON/spec.

    Strategy:
    1) Retry transient failures (timeouts/rate-limit)
    2) If still failing, fallback to cached last-good spec (if provided)
    3) If no cache, return a minimal safe spec placeholder
    """
    llm_policy = RetryPolicy(
        max_attempts=3,
        base_delay_s=1.0,
        max_delay_s=10.0,
        backoff=2.0,
        jitter_s=0.5,
        retry_on=(TimeoutError, ConnectionError, LLMError),
    )

    try:
        spec = retry(llm_call, policy=llm_policy, op_name="LLM.generate_tableau_spec")
        if cache_set:
            try:
                cache_set(spec)
            except Exception as exc:
                logger.warning("Cache set failed (ignored): %s", exc)
        return spec

    except Exception as exc:
        logger.error("LLM generation failed. Using fallback. Reason: %s", exc)

    # Fallback 1: cached
    if cache_get:
        try:
            cached = cache_get()
            if cached:
                logger.info("Using cached Tableau spec fallback.")
                return cached
        except Exception as exc:
            logger.warning("Cache get failed (ignored): %s", exc)

    # Fallback 2: minimal safe spec (so UI doesn't crash)
    return {
        "version": "fallback_v1",
        "title": "Fallback Dashboard",
        "worksheets": [],
        "dashboards": [
            {
                "name": "Dashboard 1",
                "layout": [],
                "notes": "LLM generation failed; this is a minimal fallback spec."
            }
        ],
        "warnings": ["LLM failed; generated fallback spec."],
    }